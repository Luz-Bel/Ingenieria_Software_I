    private void login(){
        String usuario = "Benjamin";
        String clave = "grupo_8";
        
        String user = txtUsuario.getText();
        String pass = txtContra.getText();
        
        if(usuario.equals(user) && clave.equals(pass)){
            Usuario log = new Usuario();
            log.setVisible(true);
            log.show();
            dispose();
        }
        
        else{
            JOptionPane.showMessageDialog(null, "Ingrese de nuevo sus datos.");
            txtUsuario.setText("");
            txtContra.setText("");
            txtUsuario.requestFocus();
        }
    }

    
